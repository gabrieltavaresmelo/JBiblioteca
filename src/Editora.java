public class Editora {

    int id;
    String nome;
    String endereco;
    String cidade;
    String estado;
    String cep;
    String telefone;

    public Editora() {}

    public Editora(int id, String nome, String endereco, String cidade, String estado, String cep, String telefone) {
        this.id = id;
        this.nome = nome;
        this.endereco = endereco;
        this.cidade = cidade;
        this.estado = estado;
        this.cep = cep;
        this.telefone = telefone;
    }
}
